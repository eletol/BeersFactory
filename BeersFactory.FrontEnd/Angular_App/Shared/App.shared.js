angular.module('App.shared', ['ngCookies', 'pascalprecht.translate', 'ngMessages', 'ui.bootstrap', 'datetimepicker.popup', 'ngDialog', 'toaster']);

     // Example of how to set default values for all dialogs
angular.module('App.shared').config(['ngDialogProvider', function (ngDialogProvider) {
            ngDialogProvider.setDefaults({
                className: 'ngdialog-theme-default',
                plain: false,
                showClose: true,
                closeByDocument: true,
                closeByEscape: true,
                appendTo: false,
                preCloseCallback: function () {
                    console.log('default pre-close callback');
                }
            });
        }]);

angular.module('App.shared').directive('myEnter', function () {
    return function (scope, element, attrs) {
        element.bind("keydown keypress", function (event) {
            if(event.which === 13) {
                scope.$apply(function (){
                    scope.$eval(attrs.myEnter);
                });

                event.preventDefault();
            }
        });
    };
});